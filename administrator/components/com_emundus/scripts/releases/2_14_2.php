namespace scripts;

class Release2_14_2Installer extends ReleaseInstaller
{
	private array $tasks = [];

	public function __construct()
	{
		parent::__construct();
	}

	public function install()
	{
		$result = ['status' => false, 'message' => ''];

		$query = $this->db->createQuery();

		try
		{
			$addedColumn = \EmundusHelperUpdate::addColumn('#__emundus_task', 'messages', 'JSON');
			if (!$addedColumn['status'])
			{
				throw new \Exception($addedColumn['message']);
			}

			$result['status'] = !in_array(false, $this->tasks);
		}
		catch (\Exception $e)
		{
			$result['status']  = false;
			$result['message'] = $e->getMessage();
		}

		return $result;
	}
}
